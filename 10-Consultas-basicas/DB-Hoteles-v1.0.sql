--Tabla categoria
CREATE TABLE categoria(
	id INT PRIMARY KEY,
	categoria VARCHAR(15) NOT NULL
);

--Tabla	hotel
CREATE TABLE hotel(
	id INT PRIMARY KEY,
	nombre VARCHAR(50),
	direccion VARCHAR(100) NOT NULL,
	telefono CHAR(13) NOT NULL,
	id_categoria INT NOT NULL
);

--tabla tipo_habitacion
CREATE TABLE tipo_habitacion(
	id INT PRIMARY KEY,
	nombre VARCHAR(10) NOT NULL,
	descripcion VARCHAR(100) NULL
);

--tabla habitacion
CREATE TABLE habitacion(
	id INT PRIMARY KEY,
	numero INT NOT NULL,
	id_tipo INT NOT NULL,
	precio MONEY NOT NULL, --por noche
	id_hotel INT NOT NULL
);

--tabla tipo_cliente
CREATE TABLE tipo_cliente(
	id INT PRIMARY KEY,
	tipo VARCHAR(50) NULL
);

--tabla pais
CREATE TABLE pais(
	id INT PRIMARY KEY,
	pais VARCHAR(15) NOT NULL
);

--tabla cliente
CREATE TABLE cliente(
	id INT PRIMARY KEY,
	nombre VARCHAR(100) NOT NULL,
	id_pais INT NULL,
	correo VARCHAR(25) NULL,
	tipo_cliente INT NOT NULL
);

--tabla servicio
CREATE TABLE servicio(
	id INT PRIMARY KEY,
	nombre VARCHAR(30) NOT NULL,
	precio MONEY NOT NULL
);

--tabla reserva
CREATE TABLE reserva(
	id INT PRIMARY KEY,
	checkin DATETIME NOT NULL,
	checkout DATETIME NOT NULL,
	id_cliente INT NOT NULL,
	id_habitacion INT NOT NULL,
);

--table resxser
CREATE TABLE extras(
	id_reserva INT NOT NULL,
	id_servicio INT NOT NULL,
	CONSTRAINT PK_resxser PRIMARY KEY (id_reserva,id_servicio)
);

--Definicion de relaciones
--categoria -> hotel
ALTER TABLE hotel ADD FOREIGN KEY (id_categoria) REFERENCES categoria (id);
--tipo_habitacion -> habitacion
ALTER TABLE habitacion ADD FOREIGN KEY (id_tipo) REFERENCES tipo_habitacion (id);
--hotel -> habitacion
ALTER TABLE habitacion ADD FOREIGN KEY (id_hotel) REFERENCES hotel (id);
--tipo_cliente -> cliente
ALTER TABLE cliente ADD FOREIGN KEY (tipo_cliente) REFERENCES tipo_cliente (id);
--pais -> cliente
ALTER TABLE cliente ADD FOREIGN KEY (id_pais) REFERENCES pais (id);
--cliente -> reserva
ALTER TABLE reserva ADD FOREIGN KEY (id_cliente) REFERENCES cliente (id);
--habitacion -> reserva
ALTER TABLE reserva ADD FOREIGN KEY (id_habitacion) REFERENCES habitacion (id);
--reserva -> extras
ALTER TABLE extras ADD FOREIGN KEY (id_reserva) REFERENCES reserva (id);
--servicio -> extras
ALTER TABLE extras ADD FOREIGN KEY (id_servicio) REFERENCES servicio (id);

